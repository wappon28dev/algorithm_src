#include <stdio.h>

int main(void)
{
    char c[6][6] = {
        {' ', ' ', ' ', ' ', ' ', ' '},
        {' ', '*', '*', '*', '*', '*'},
        {' ', '*', '*', '*', '*', ' '},
        {' ', '*', '*', '*', ' ', ' '},
        {' ', '*', '*', ' ', ' ', ' '},
        {' ', '*', ' ', ' ', ' ', ' '},
    };

    int i, j;

    for (i = 1; i <= 5; i++)
    {
        for (j = 1; j <= 5; j++)
        {
            printf("%2c", c[i][j]);
        }
        printf("\n");
    }
    return 0;
}

