#include <stdio.h>

int main(void)
{

    char c[6][6] = {
        {' ', ' ', ' ', ' ', ' ', ' '},
        {' ', '*', '*', '*', '*', '*'},
        {' ', '*', '*', '*', '*', ' '},
        {' ', '*', '*', '*', ' ', ' '},
        {' ', '*', '*', ' ', ' ', ' '},
        {' ', '*', ' ', ' ', ' ', ' '},
    };

    char ch[6][6];
    int i, j;

    for (i = 1; i <= 5; i++)
    {
        for (j = 1; j <= 5; j++)
        {
            ch[i][j] = c[5 - (j - 1)][i];
            printf("%2c", ch[i][j]);
        }
        printf("\n");
    }

    return 0;
}

